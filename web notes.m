For example, the following sets the default FontName used by text 
to Symbol for the current axes only:

set(gca, 'DefaultTextFontName', 'symbol')

To specify the default FontName for the current figure, use gcf 
as the handle. To specify the default for the entire session, use 0.

Setting the default FontName of text will affect all text objects. 
Setting the default FontName will set the font used for axis labels, 
titles, and tick mark labels.



